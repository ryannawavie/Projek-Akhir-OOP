    private void tblRegistrationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRegistrationMouseClicked
        int row =tblRegistration.getSelectedRow();   
        String diklik=(tblRegistration.getModel().getValueAt(row,0).toString());
        String perintah ="SELECT * from customer_table where custcode='"+diklik+"'";

         try {
             pst = conn.prepareStatement(perintah);
             rst = pst.executeQuery();
             
             if(rst.next()){
                 
                 
                 
                String tulisan1= rst.getString("custcode");
                txtCustomercode.setText(tulisan1);
                
                String tulisan2= rst.getString("name");
                txtName.setText(tulisan2);
                
                String tulisan3= rst.getString("phonenum");
                txtPhoneNumber.setText(tulisan3);
                
                String tulisan4= rst.getString("email");
                txtEmail.setText(tulisan4);
                
                String tulisan5= rst.getString("roomnum");
                txtRoom.setText(tulisan5);
                
               
             }
             
         } catch (SQLException ex) {
             System.out.println(ex);
         } 
        
    }//GEN-LAST:event_tblRegistrationMouseClicked

