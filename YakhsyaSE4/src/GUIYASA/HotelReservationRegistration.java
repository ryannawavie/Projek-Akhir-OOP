     private void tampilkan_data(){
        String perintah = "SELECT * FROM customer_table";

        try {
            pst = conn.prepareStatement(perintah);
            rst = pst.executeQuery();
            DefaultTableModel admin = new DefaultTableModel();
            admin.addColumn("Customer Code");
            admin.addColumn("Name");
            admin.addColumn("Phone Number");
            admin.addColumn("Email");
            admin.addColumn("Room Number");
            

           
            while (rst.next()){
                admin.addRow(new Object[]{
                    rst.getString(1),
                    rst.getString(2),
                    rst.getString(3),
                    rst.getString(4),
                    rst.getString(5)
                });

            }
            tblRegistration.setModel(admin);

        } catch (SQLException ex) {
            System.out.println(ex);
        }

 }
     
     
     
     
